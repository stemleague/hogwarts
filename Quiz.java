public class Quiz {
  public static void main(String[] args) {
    // Question One
    String favoriteColor = "blue";
    if (favoriteColor == "blue") {
      System.out.println("Wise one, you must be a Ravenclaw!");
    } else if (favoriteColor == "red") {
      System.out.println("Brave one, you must be a Gryffindor!");
    } else if (favoriteColor == "green") {
      System.out.println("Ambitious one, you must be a Slytherin!");
    } else if (favoriteColor == "yellow") {
      System.out.println("Extraordinary one, you must be a Hufflepuff!");
    }

    // Question Two
    boolean triedOutForQuidditch = true;
    if (triedOutForQuidditch) {
      System.out.println("Good luck at the Quidditch try outs!");
    } else {
      System.out.println("Excited to cheer on our Quidditch team in the stands!");
    }
  }
}
